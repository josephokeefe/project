      | App (ex1, ex2) -> (match eval_l ex1 env with
                          | Closure (Fun(v, ex), env2) -> eval_l ex (extend env2 v (ref (eval_l ex2 env)))                               
                          | _ -> raise (EvalError "Not a function"))
                          
